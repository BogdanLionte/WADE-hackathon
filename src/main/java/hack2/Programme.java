package hack2;

public class Programme {
    private int rmuId;
    private String name;
    private String domain;
    private int domainId;
    private String scienceBranch;
    private String fundamentalDomain;
    private String studyCycle;
    private int studyCycleId;
    private int year;

    public int getRmuId() {
        return rmuId;
    }

    public void setRmuId(int rmuId) {
        this.rmuId = rmuId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public int getDomainId() {
        return domainId;
    }

    public void setDomainId(int domainId) {
        this.domainId = domainId;
    }

    public String getScienceBranch() {
        return scienceBranch;
    }

    public void setScienceBranch(String scienceBranch) {
        this.scienceBranch = scienceBranch;
    }

    public String getFundamentalDomain() {
        return fundamentalDomain;
    }

    public void setFundamentalDomain(String fundamentalDomain) {
        this.fundamentalDomain = fundamentalDomain;
    }

    public String getStudyCycle() {
        return studyCycle;
    }

    public void setStudyCycle(String studyCycle) {
        this.studyCycle = studyCycle;
    }

    public int getStudyCycleId() {
        return studyCycleId;
    }

    public void setStudyCycleId(int studyCycleId) {
        this.studyCycleId = studyCycleId;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
