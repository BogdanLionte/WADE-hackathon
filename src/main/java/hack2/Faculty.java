package hack2;

public class Faculty {

    private int rmuId;
    private String name;
    private String university;
    private int universityId;
    private int isDoctoralSchool;
    private int year;

    public int getRmuId() {
        return rmuId;
    }

    public void setRmuId(int rmuId) {
        this.rmuId = rmuId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUniversity() {
        return university;
    }

    public void setUniversity(String university) {
        this.university = university;
    }

    public int getUniversityId() {
        return universityId;
    }

    public void setUniversityId(int universityId) {
        this.universityId = universityId;
    }

    public int getIsDoctoralSchool() {
        return isDoctoralSchool;
    }

    public void setIsDoctoralSchool(int isDoctoralSchool) {
        this.isDoctoralSchool = isDoctoralSchool;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
